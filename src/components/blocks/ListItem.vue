<template>
    <li :class="classList"><div class="robo-list-item-content"><slot/></div></li>
</template>

<script>
export default {
  name: 'RoboListItem',


  props: {

    color: {
      type: String,
      default: 'dark',
      validator: function (value) {
        return ['dark', 'green', 'gray'].indexOf(value) !== -1;
      }
    },
  },

  computed: {
    classList() {
      return {
        [`robo-list-item`]: true,
        [`robo-list-item-${this.color}`]: this.color,
      };
    }
  },

}
</script>

